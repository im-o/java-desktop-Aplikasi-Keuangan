        int varEkuitas = varSAwal+varLabaDitahan;
        jTextFieldEkuitas.setText(Integer.toString(varEkuitas));
        jTextFieldLabaBersih.setText(Integer.toString(varTotalSaldo));
        jTextFieldLabaBersihEkuitas.setText(Integer.toString(varTotalSaldo));
        
        
