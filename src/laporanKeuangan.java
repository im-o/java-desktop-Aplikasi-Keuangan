    private void getAll(){
        jurnalPembelian jPembelian = new jurnalPembelian();
        varPenjualan = jPembelian.totalPenjualanK;
        String potPenj = jTextFieldPotonganPenj.getText();
        varPenjualanBersih = varPenjualan - Integer.valueOf(potPenj);
        jTextField1Penjualan.setText(Integer.toString(varPenjualan));
        jTextFieldPenjualanBersih.setText(Integer.toString(varPenjualanBersih));
        
        varPembelian =jPembelian.totalPembelianD;
        varPotPembelian = jPembelian.totalPotPembelianK;
        varPembelianBersih = varPembelian - Integer.valueOf(varPotPembelian);
        jTextFieldPembelian.setText(Integer.toString(varPembelian));
        jTextFieldPotPembelian.setText(Integer.toString(varPotPembelian));
        jTextFieldPembelianBersih.setText(Integer.toString(varPembelianBersih));
        
        varHpp = varPembelianBersih;
        jTextFieldHpp.setText(Integer.toString(varHpp));
        
        varLabaKotor = varPenjualanBersih - varHpp;
        jTextFieldLabaKotor.setText(Integer.toString(varLabaKotor));
        
        varBeban = jPembelian.totalBebanD;
        jTextFieldBebanOperasi.setText(Integer.toString(varBeban));
        
        varTotalSaldo = varLabaKotor - varBeban;
        jSpinnerLabaBersih.setValue(varTotalSaldo);
        
    }
    
